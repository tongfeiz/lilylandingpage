import fs from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import * as topojson from 'topojson-client'
import { geoPath, geoAlbersUsa } from 'd3-geo'
import topology from 'us-atlas/states-10m.json' with { type: 'json' }

const __dirname = dirname(fileURLToPath(import.meta.url))

const WIDTH = 1000
const HEIGHT = 600
const PAD = 12

const allStates = topojson.feature(topology, topology.objects.states)
const states = {
  type: 'FeatureCollection',
  features: allStates.features.filter(
    (feature) =>
      feature.properties?.name !== 'Alaska' &&
      feature.properties?.name !== 'Hawaii'
  ),
}
const projection = geoAlbersUsa().fitExtent(
  [
    [PAD, PAD],
    [WIDTH - PAD, HEIGHT - PAD],
  ],
  states
)
const pathGen = geoPath(projection)
const d = pathGen(states)

/** @type {Array<[number, number]>} lon, lat */
const markerLonLat = [
  [-122.32, 38.02], // Bay Area — N, away from LA cluster
  [-118.48, 34.12], // LA — W
  [-117.0, 32.38], // San Diego — SE vs LA
  [-84.2, 42.45], // SE Michigan
  [-75.0, 39.78], // Philadelphia
  [-72.75, 41.2], // NY / CT — E
  [-73.98, 39.35], // NJ — E of Philly
  [-77.6, 35.15], // Mid-Atlantic south — distinct from Acela cluster
]

const markerPixels = markerLonLat.map(([lon, lat]) => {
  const p = projection([lon, lat])
  return p ? { x: p[0], y: p[1] } : { x: 0, y: 0 }
})

const outPath = `// Auto-generated by scripts/generate-us-map-path.mjs
export const US_MAP_VIEWBOX = '0 0 ${WIDTH} ${HEIGHT}'
export const US_CONTIGUOUS_PATH_D = ${JSON.stringify(d)}
export const US_DISTRICT_MARKER_PIXELS = ${JSON.stringify(markerPixels)}
`
fs.writeFileSync(join(__dirname, '../src/data/usContiguousPath.js'), outPath)
console.log('Wrote src/data/usContiguousPath.js', { pathLen: d.length, markers: markerPixels.length })
